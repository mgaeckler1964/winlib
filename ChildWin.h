class ChildWindow : public CallbackWindow
{
	private:

//	static void registerClass( void );

	virtual STRING getWindowClassName( void ) const = 0;

	protected:
	void notifyParent( void )
	{
		getParent()->postMessage( WM_COMMAND, getId() );
	}
	public:
	ChildWindow( BasicWindow *owner = NULL ) : CallbackWindow( owner )
	{
		setStyle( WS_CHILD|WS_VISIBLE );
	}

	SuccessCode create( BasicWindow * parent )
	{
		addStyle( WS_CHILD );
		return CallbackWindow::create( parent );
	}

};

