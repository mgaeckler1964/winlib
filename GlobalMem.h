class GlobalLocker
{
	HGLOBAL	m_hMem;
	void	*m_buffer;

	public:
	GlobalLocker(HGLOBAL hMem) : m_hMem(hMem), m_buffer(nullptr)
	{}
	~GlobalLocker()
	{
		if( m_buffer )
			GlobalUnlock( m_hMem );
	}
	HGLOBAL getHandle() const
	{
		return m_hMem;
	}
	void *getPtr()
	{
		if( !m_buffer && m_hMem )
			m_buffer = GlobalLock( m_hMem );
		return m_buffer;
	}
	size_t size() const
	{
		return GlobalSize(m_hMem);
	}
	bool is_open() const
	{
		return m_hMem ? true : false;
	}
};
